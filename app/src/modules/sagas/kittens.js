function* getKittens({payload}) {
  const netinfo = yield select(state => state.netinfo);
  if (netinfo.isOnline) {
    let rangeKitten = _.map(_.range(0, payload.count), (item) => {
      return {
        imageUrl: `http://placekitten.com/200/300?image=${Math.floor(Math.random() * 16) + 1}`,
        label: names[Math.floor(Math.random() * 50)],
        id: uuid.v4()
      }
    });
    yield put(kittenActions.setKittens(rangeKitten));
  }
}

function* userWatcher() {
  yield takeLatest(kittenActionsType.GET_KITTENS, getKittens);
}

